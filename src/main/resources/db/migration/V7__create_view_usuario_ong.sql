CREATE
OR REPLACE VIEW v_login_identity AS
SELECT
    id,
    email,
    senha,
    nome_usuario,
    role
FROM
    usuario
UNION
SELECT
    id,
    email,
    senha,
    nome_usuario,
    role
FROM
    ong
UNION
SELECT
    id,
    email,
    senha,
    nome_usuario,
    role
from
    administrador;