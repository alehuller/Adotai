CREATE
OR REPLACE VIEW v_login_identity AS
SELECT
    id,
    email,
    senha,
    nome_usuario,
    role,
    status
FROM
    usuario
UNION
SELECT
    id,
    email,
    senha,
    nome_usuario,
    role,
    status
FROM
    ong
UNION
SELECT
    id,
    email,
    senha,
    nome_usuario,
    role,
    status
from
    administrador;